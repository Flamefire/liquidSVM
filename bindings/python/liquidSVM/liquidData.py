class LiquidData:
    """This class helps to organize train/test splits and targets. It has a
    train and a test attribute, of which each has a data, target, and DESCR
    attribute as known from sklearn. This looks at several locations to find
    a name.train.csv and name.test.csv. If it does then it loads or downloads
    it, parses it, and returns an liquidData-object. The files also can be
    gzipped having names name.train.csv.gz and name.test.csv.gz.

    Included in the package are 'banana-bc', 'banana-mc', 'covtype.1000', and
    'reg-1d'.

    Parameters
    ----------
    name : str
        The base name of a train/test splitted data set to load.

    Attributes
    ----------
    name : str
        The name of the data sets
    train : Bunch
        The training data set.
    test : Bunch
        The test set including labels
    loc : str
        The location where the data was found
    """
    def __init__(self, name):
        train_data = None
        test_data = None
        theLoc = None
        for loc in ["./", os.path.expanduser('~/liquidData/'),
                    pkg_resources.resource_filename(__name__, 'data/'),
                    'http://www.isa.uni-stuttgart.de/liquidData/']:
            # print('Trying to load from ' + loc)
            for ext in ['.csv', '.csv.gz']:
                try:
                    # print(loc+name+'.train'+ext)
                    trainFileName = loc + name + '.train' + ext
                    testFileName = loc + name + '.train' + ext
                    train_data = np.genfromtxt(trainFileName, delimiter=", ")
                    test_data = np.genfromtxt(testFileName, delimiter=", ")
                    theLoc = loc
                    break
                except:
                    pass
            if theLoc is not None:
                break
        if train_data is None or test_data is None:
            raise FileNotFoundError

        self.name = name
        self.train = self.__bunch(train_data, name + " (train)")
        self.test = self.__bunch(test_data, name + " (test)")
        self.loc = theLoc

    def __bunch(self, data, descr):
        # ret = []
        # ret.data = data[:,1:]
        # ret.target = data[:,0]
        # ret.DESCR = descr
        return Bunch(data=data[:, 1:], target=data[:, 0], DESCR=descr)
        # return ret

    def fromData(self, train_x, train_y, test_x, test_y):
        """Creates a LiquidData from given `np.array`s

        Parameters
        ----------
        train_x : np.array
            Train features.
        train_y : np.array
            Train labels
        test_x : np.array
            Test features.
        test_y : np.array
            Test labels.

        Returns
        -------
        LiquidData
        """
        self.train = Bunch(data=train_x, target=train_y, DESCR='')
        self.test = Bunch(data=test_x, target=test_y, DESCR='')


class Bunch(dict):
    """ """
    def __init__(self, *args, **kwds):
        super(Bunch, self).__init__(*args, **kwds)
        self.__dict__ = self
